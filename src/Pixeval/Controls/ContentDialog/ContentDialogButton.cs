namespace Pixeval.Controls;

/// <summary>
/// Identifies a button in a <see cref="ContentDialog"/>.
/// </summary>
public enum ContentDialogButton
{
    /// <summary>
    /// No dialog button.
    /// </summary>
    None,

    /// <summary>
    /// The primary action button.
    /// </summary>
    Primary,

    /// <summary>
    /// The secondary action button.
    /// </summary>
    Secondary,

    /// <summary>
    /// The close action button.
    /// </summary>
    Close
}
